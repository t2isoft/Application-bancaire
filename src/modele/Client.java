package modele;

public class Client {
	
	private int Numcli;
	private String Civilite;
	private String Nom;
	private String Prenom;
	private String Datenaisse;
	private String Adresse;
	private String Telephone;
	private String LibelleAg;
	public int getNumcli() {
		return Numcli;
	}
	public void setNumcli(int numcli) {
		Numcli = numcli;
	}
	public String getCivilite() {
		return Civilite;
	}
	public void setCivilite(String civilite) {
		Civilite = civilite;
	}
	public String getNom() {
		return Nom;
	}
	public void setNom(String nom) {
		Nom = nom;
	}
	public String getPrenom() {
		return Prenom;
	}
	public void setPrenom(String prenom) {
		Prenom = prenom;
	}
	public String getDatenaisse() {
		return Datenaisse;
	}
	public void setDatenaisse(String datenaisse) {
		Datenaisse = datenaisse;
	}
	public String getAdresse() {
		return Adresse;
	}
	public void setAdresse(String adresse) {
		Adresse = adresse;
	}
	public String getTelephone() {
		return Telephone;
	}
	public void setTelephone(String telephone) {
		Telephone = telephone;
	}
	public String getLibelleA() {
		return LibelleAg;
	}
	public void setLibelleA(String libellea) {
		LibelleAg = libellea;
	}

}
