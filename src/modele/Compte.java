package modele;

public class Compte {
	
	private int numCp;
	private String libellecp;
	private String senscp;
	private int solde;
	private int numcli;
	public int getNumCp() {
		return numCp;
	}
	public void setNumCp(int numCp) {
		this.numCp = numCp;
	}
	public String getLibellecp() {
		return libellecp;
	}
	public void setLibellecp(String libellecp) {
		this.libellecp = libellecp;
	}
	public String getSenscp() {
		return senscp;
	}
	public void setSenscp(String senscp) {
		this.senscp = senscp;
	}
	public int getSolde() {
		return solde;
	}
	public void setSolde(int solde) {
		this.solde = solde;
	}
	public int getNumcli() {
		return numcli;
	}
	public void setNumcli(int numcli) {
		this.numcli = numcli;
	}
	
	
	

}
